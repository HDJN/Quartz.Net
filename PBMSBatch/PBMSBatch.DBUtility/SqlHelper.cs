        public static void DataToSql(string connectionString, DataTable dt, string tableName)
        {
            System.Data.SqlClient.SqlConnection conn = new System.Data.SqlClient.SqlConnection(connectionString);

            conn.Open();
            System.Data.SqlClient.SqlBulkCopy sbc = new System.Data.SqlClient.SqlBulkCopy(conn);
            sbc.DestinationTableName = tableName;

            //将数据集合和目标服务器的字段对应
            for (int q = 0; q < dt.Columns.Count; q++)
            {
                sbc.ColumnMappings.Add(dt.Columns[q].ColumnName, dt.Columns[q].ColumnName);
            }
            try
            {
                sbc.WriteToServer(dt);
            }
            catch (Exception ex)
            {

            }
            finally
            {
                dt.Dispose();
                conn.Close();
                sbc.Close();
            }


        }
        /// <summary>
        /// 执行SQL语句，返回数据集
        /// </summary>
        /// <param name="sqlText"></param>
        /// <param name="cmdType"></param>
        /// <param name="parms"></param>
        /// <returns></returns>
        public static DataSet DataSet(string connectionString, string sqlText, CommandType cmdType, SqlParameter[] parms)
        {
            DataSet ds = new DataSet();
            SqlDataAdapter sda = new SqlDataAdapter(sqlText, connectionString);
            sda.SelectCommand.CommandType = cmdType;
            if (parms != null)
            {
                foreach (SqlParameter parm in parms)
                {
                    sda.SelectCommand.Parameters.Add(parm);
                }
            }
            sda.Fill(ds, "DataList");
            sda.SelectCommand.Parameters.Clear();
            sda.Dispose();

            return ds;
        }
