        ///<summary>
        ///批量更新
        ///</summary>
        ///<param name="cmdSql"></param>
        ///<param name="dtList"></param>
        ///<returns></returns>
        public static int Update(string connectionString, DataTable table)
        {
            int res = 0;
            using (SqlConnection sqlconn = new SqlConnection(connectionString))
            {
                sqlconn.Open();
                //事务 
                SqlTransaction tran = sqlconn.BeginTransaction(IsolationLevel.ReadCommitted);
                try
                {
                    table.AcceptChanges();
                    foreach (DataRow dr in table.Rows)
                    {
                        // 所有行设为修改状态   
                        dr.SetModified();
                    }
                    // 为Adapter定位目标表 
                    SqlCommand cmd = new SqlCommand("select * from " + table.TableName + " where 1=0", sqlconn, tran);
                    SqlDataAdapter da = new SqlDataAdapter(cmd);
                    SqlCommandBuilder sqlCmdBuilder = new SqlCommandBuilder(da);
                    da.AcceptChangesDuringUpdate = false;
                    SqlCommand updatecmd = new SqlCommand("UPDATE " + table.TableName + " SET [ParaValue1] = @ParaValue1,[ParaValue2] = @ParaValue2,[ParaValue3] = @ParaValue3,[ParaValue4] = @ParaValue4,[ParaValue5] = @ParaValue5,[ParaValue6] = @ParaValue6 where OperationId=@OperationId");
                    // 不修改源DataTable   
                    updatecmd.UpdatedRowSource = UpdateRowSource.None;
                    da.UpdateCommand = updatecmd;
                    da.UpdateCommand.Parameters.Add("@ParaValue1", SqlDbType.Decimal, 9, "ParaValue1");
                    da.UpdateCommand.Parameters.Add("@ParaValue2", SqlDbType.NVarChar, 50, "ParaValue2");
                    da.UpdateCommand.Parameters.Add("@ParaValue3", SqlDbType.Decimal, 0, "ParaValue3");
                    da.UpdateCommand.Parameters.Add("@ParaValue4", SqlDbType.Decimal, 9, "ParaValue4");
                    da.UpdateCommand.Parameters.Add("@ParaValue5", SqlDbType.Decimal, 9, "ParaValue5");
                    da.UpdateCommand.Parameters.Add("@ParaValue6", SqlDbType.Decimal, 9, "ParaValue6");
                    da.UpdateCommand.Parameters.Add("@OperationId", SqlDbType.Int, 4, "OperationId");
                    da.UpdateBatchSize = 1000;
                    res = da.Update(table);
                    table.AcceptChanges();
                    tran.Commit();
                    sqlconn.Close();
                }
                catch
                {
                    tran.Rollback();
                    return -1;
                }
            };
            return res;

        }
